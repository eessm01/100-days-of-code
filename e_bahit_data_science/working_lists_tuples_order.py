"""Ciencia de datos con Python 2da. edición.

Manipulacion de tuplas y listas. Métodos de ordenamiento.
"""

nombres_masculinos = ['Alvaro', 'Jacinto', 'Ricky', 'Edgardo', 'David', 'Pablo', 'Pedro', 'Julian', 'Mario']
print(nombres_masculinos)

# ordenar una lista en reversa
nombres_masculinos.reverse()
print(nombres_masculinos)

# ordenar una lista en forma ascendente 
nombres_masculinos.sort()
print(nombres_masculinos)

# ordenar una lista de forma descendente
nombres_masculinos.sort(reverse=True)
print(nombres_masculinos)

